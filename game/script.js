function showDetails() {
  var x = document.getElementById("DetailsShowHide");
    x.style.display = "block";
   x = document.getElementById("DetailsShow");
      x.style.display = "none";
   x = document.getElementById("DetailsHide");
      x.style.display = "block";
}

function hideDetails() {
  var x = document.getElementById("DetailsShowHide");
    x.style.display = "none";
    x = document.getElementById("DetailsShow");
      x.style.display = "block";
   x = document.getElementById("DetailsHide");
      x.style.display = "none";

}